# Complete the climbingLeaderboard function below.
def climbingLeaderboard(scores, alice):
    scores=sorted(list(set(scores)))
    n=len(scores)
    ranks=[]
    ind=0
    for i in alice:
        while(n>ind and i>=scores[ind]):
            ind+=1
        ranks.append(n+1-ind) 
    return ranks

